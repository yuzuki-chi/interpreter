package newlang3;

public enum ValueType {
	VOID,

    /**
     * INTEGER: ValueTypeがINTEGERってことだよ 
     */
    INTEGER,

    /**
     * STRING: ValueTypeがSTRINGってことだよ
     */
    STRING,

    /**
     * DOUBLE: ValueTypeがDOUBLEってことだよ
     */
    DOUBLE,
    
    /**
     * BOOL: ValueTypeがBOOLEANってことだよ
     */
    BOOL,
}
